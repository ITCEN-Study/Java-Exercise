package day1;

public class Exercise4 {

	public static void main(String[] args) {
		int number = 100;
		int result;
		
		System.out.print("덧셈 연산의 결과 : ");
		System.out.println(result = number + 10);
		
		System.out.print("뺄셈 연산의 결과 : ");
		System.out.println(result = number - 10);
		
		System.out.print("곱셈 연산의 결과 : ");
		System.out.println(result = number * 10);
		
		System.out.print("나눗셈 연산의 결과 : ");
		System.out.println(result = number / 10);
	}

}
